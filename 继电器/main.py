#******************************************************************************/
# import Pin/time
#******************************************************************************/
from pyb import Pin
import time


#******************************************************************************/
# 继电器模块连接到PYBOARD开发板Y5引脚
#******************************************************************************/
relay = Pin('Y5',Pin.OUT_PP)


#******************************************************************************/
# relay.low() - 常开  relay.high() - 常闭   
#******************************************************************************/
while 1:
	relay.low() 
	time.sleep_ms(1000)
	relay.high()
	time.sleep_ms(1000)


