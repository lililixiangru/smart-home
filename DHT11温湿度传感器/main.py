#******************************************************************************/
# import LCD1602/DHT11
#******************************************************************************/
from dht11 import DHT11
import time
from lcd1602 import LCD1602


#******************************************************************************/
# LCD1602/DS18B20与pyboard硬件连接引脚对应关系
#******************************************************************************/
DHT11 = DHT11('B13')
LCD1602 = LCD1602('X1', 'X2', 'X3', 'X4', 'X5',
                  'X6', 'X7', 'X8', 'Y9', 'Y10', 'Y11')


#******************************************************************************/
# DHT11读取到的结果存放在resultList
#******************************************************************************/
resultList = [0, 0, 0, 0, 0]


#******************************************************************************/
# DHT11采集数据
#******************************************************************************/
DHT11.read_bytes(resultList)


#******************************************************************************/
# LCD1602模块显示结果
#******************************************************************************/
LCD1602.Init()
LCD1602.Show_String(1,1,'                ')
LCD1602.Show_String(2,1,'                ')
LCD1602.Show_String(1,1,'H:')
LCD1602.Show_String(1,3,str(resultList[0])+'.'+str(resultList[1])+'%')
LCD1602.Show_String(2,1,'T:')
LCD1602.Show_String(2,3,str(resultList[2])+'.'+str(resultList[3]))
LCD1602.Show_String(1,10,'DHT11')
LCD1602.Show_String(2,8,'H&T TEST')
time.sleep_ms(1000)





